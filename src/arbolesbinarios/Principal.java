    public void cargarArbol(){
    arbolito = new Arbol(jTextField1.getText());
    //Nivel 1
    arbolito.insertarIzquierda(jTextField2.getText(), arbolito.getRaiz());
    arbolito.insertarDerecha(jTextField3.getText(), arbolito.getRaiz());
    //Nivel 2
    arbolito.insertarIzquierda(jTextField4.getText(), arbolito.getRaiz().getIzquirda());
    arbolito.insertarDerecha(jTextField5.getText(), arbolito.getRaiz().getIzquirda());
    arbolito.insertarIzquierda(jTextField6.getText(), arbolito.getRaiz().getDerecha());
    arbolito.insertarDerecha(jTextField7.getText(), arbolito.getRaiz().getDerecha());
    //Nivel 3
    arbolito.insertarIzquierda(jTextField8.getText(), arbolito.getRaiz().getDerecha().getIzquirda());
    arbolito.insertarDerecha(jTextField9.getText(), arbolito.getRaiz().getDerecha().getIzquirda());
    //Nivel 4
    arbolito.insertarIzquierda(jTextField10.getText(), arbolito.getRaiz().getDerecha().getIzquirda().getIzquirda());
    arbolito.insertarDerecha(jTextField11.getText(), arbolito.getRaiz().getDerecha().getIzquirda().getIzquirda());
}

